package Exercicio3;

public class Pacote_do_Exercicio3 {
	
	private String email_estudantes;
	private String nome_estudantes;
	
	public Pacote_do_Exercicio3(String email_estudantes, String nome_estudantes) {
		
		this.email_estudantes = email_estudantes;
		this.nome_estudantes = nome_estudantes;
	}

	public String getEmail_estudantes() {
		return email_estudantes;
	}

	public void setEmail_estudantes(String email_estudantes) {
		this.email_estudantes = email_estudantes;
	}

	public String getNome_estudantes() {
		return nome_estudantes;
	}

	public void setNome_estudantes(String nome_estudantes) {
		this.nome_estudantes = nome_estudantes;
	}
	

	
	
	
}